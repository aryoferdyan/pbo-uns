public class Operasi {
    
    int jumlahkan(int a, int b){
        return (a+b);
    }
    	
    int jumlahkan(int a, int b, int c){
        return (a+b+c);
    }
    
    double jumlahkan(double a, double b){
        return (a+b);
    }
    
    double jumlahkan(double a, double b, double c){
        return (a+b+c);
    }
    
    //PERKALIAN
    double kalikan(double x, double y){
        return (x*y);
    }
    
    double kalikan(double x, double y, double z){
        return (x*y*z);
    }
    
    int kalikan(int x, int y){
        return (x*y);
    }
    
    int kalikan(int x, int y, int z){
        return (x*y*z);
    }
}
